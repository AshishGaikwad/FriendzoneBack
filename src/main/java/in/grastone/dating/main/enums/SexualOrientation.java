package in.grastone.dating.main.enums;
public enum SexualOrientation {
    // Attracted to the opposite sex
    HETEROSEXUAL,

    // Attracted to the same sex
    HOMOSEXUAL,

    // Attracted to both sexes
    BISEXUAL,

    // Attracted regardless of sex or gender
    PANSEXUAL,

    // Not feeling sexual attraction
    ASEXUAL,

    // A broad term for various identities
    QUEER,

    // Exploring or unsure of their orientation
    QUESTIONING,

    // For other orientations not listed
    OTHER,

    // Choose not to disclose their orientation
    PREFER_NOT_TO_SAY
}
